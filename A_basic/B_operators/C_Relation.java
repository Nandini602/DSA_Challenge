

public class C_Relation{
    public static void  main(String args[]){
         
        //  == != >  < >= <=   -- always give ans in true or flase 

        int a = 12;
        int b= 13; 

        System.out.println(" == " + (a==b));
        System.out.println(" != " + (a!=b));
        System.out.println("> " + (a>b));
        System.out.println("< " + (a<b));
        System.out.println(">= " +(a>=b));
        System.out.println("<= " + (a<=b));
    }
}